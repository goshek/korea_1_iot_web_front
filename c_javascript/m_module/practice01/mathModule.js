/*
    mathModule.js
    
    사칙 연산의 함수를 정의
    >> 각 함수는 인자값 2개를 받아 연산 후
    : add(더하기), substract(빼기), multiply(곱하기), divide(나누기)
*/
export function add(x,y){
    return x+y;
}

export default function substract(x, y){
    return x-y;
}

export function multiply(x,y){
    return x*y;
}

export function divide(x,y){
    return x/y;
}